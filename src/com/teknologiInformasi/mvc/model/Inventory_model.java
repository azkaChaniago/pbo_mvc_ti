public class Inventory_model {

    private String inv_id, inv_name, inv_capital, inv_sale, inv_stock;

    public String getInv_id() {
        return inv_id;
    }

    public void setInv_id(String inv_id) {
        this.inv_id = inv_id;
    }

    public String getInv_name() {
        return inv_name;
    }

    public void setInv_name(String inv_name) {
        this.inv_name = inv_name;
    }

    public String getInv_capital() {
        return inv_capital;
    }

    public void setInv_capital(String inv_capital) {
        this.inv_capital = inv_capital;
    }

    public String getInv_sale() {
        return inv_sale;
    }

    public void setInv_sale(String inv_sale) {
        this.inv_sale = inv_sale;
    }

    public String getInv_stock() {
        return inv_stock;
    }

    public void setInv_stock(String inv_stock) {
        this.inv_stock = inv_stock;
    }     
    
}
