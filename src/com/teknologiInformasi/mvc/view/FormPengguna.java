    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        // TODO add your handling code here:
        if (verifyData()) {
            jTable1.setRowSorter(null);
            model = new User_model();
            model.setUser_id(idpenggunaTextField.getText());
            model.setUser_name(namaTextField.getText());
            model.setUser_address(alamatTextField.getText());
            model.setUser_phone(teleponTextField.getText());
            model.setUser_auth(levelComboBox.getSelectedItem().toString());
            model.setUser_password(passwordPasswordField.getText());
            
            try {
                if (sql.insert(model)) {
                    table.insert(model);
                    clear();
                    notifLabel.setText("Berhasil disimpan ..");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(FormPengguna.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_simpanButtonActionPerformed

    private void ubahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahButtonActionPerformed
        // TODO add your handling code here:
        int n = table.search(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        if (n >= 0 && verifyData()) {
            model = new User_model();
            model.setUser_id(idpenggunaTextField.getText());
            model.setUser_name(namaTextField.getText());
            model.setUser_address(alamatTextField.getText());
            model.setUser_phone(teleponTextField.getText());
            model.setUser_auth(levelComboBox.getSelectedItem().toString());
            model.setUser_password(passwordPasswordField.getText());
            
            try {
                if (sql.update(model)) {
                    table.update(n, model);
                    clear();
                    notifLabel.setText("Berhasil diubah");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(FormPengguna.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            notifLabel.setText("Harus pilih data");
        }
    }//GEN-LAST:event_ubahButtonActionPerformed

    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        // TODO add your handling code here:
        int n = table.search(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        
        if (n >= 0) {
            try {
                if (sql.delete(table.field_data.get(n).getUser_id())) {
                    table.delete(n);
                    clear();
                    notifLabel.setText("Berhasil dihapus ..");
                } else {
                    notifLabel.setText("Belum ada data yang dipilih ..");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(FormPengguna.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_hapusButtonActionPerformed

