    private boolean verifyData() {
        boolean status = true;
        if (idpenggunaTextField.getText().equals(""))
            status = false;
        if (namaTextField.getText().equals(""))
            status = false;
        if (levelComboBox.getSelectedIndex() == 0)
            status = false;
        if (status == false)
            JOptionPane.showMessageDialog(null, "ID, Nama dan Level wajib diisi");
        
        return status;
    }
    
    private void newFilter() {
        sorter = new TableRowSorter<TableModel>(table);
        jTable1.setRowSorter(sorter);
        
        RowFilter<TableModel, Object> res = null;
        try {
            res = RowFilter.regexFilter(searchTextField.getText());
        } catch (java.util.regex.PatternSyntaxException e) {
            JOptionPane.showMessageDialog(null, "Pencarian gagal " + e.getMessage());
        }
    }
    
    

