    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        
        if (jTable1.getSelectedColumnCount() == 1) {
            idpenggunaTextField.setText(jTable1.getValueAt(row, 0).toString());
            idpenggunaTextField.setEditable(false);
            namaTextField.setText(jTable1.getValueAt(row, 1).toString());
            alamatTextField.setText(jTable1.getValueAt(row, 2).toString());
            teleponTextField.setText(jTable1.getValueAt(row, 3).toString());
            levelComboBox.setSelectedItem(jTable1.getValueAt(row, 4).toString());
            
            buttonRules(true, true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

